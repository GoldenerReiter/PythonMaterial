# Variables store information that we can use in our programs

john = 420

print(john)

bonachon = 69

garfield = john + bonachon

print(garfield)

# Variables must start from a lowercase of underscore
# Also, they can be only letters, numbers or underscores
# They are case sensitive
# Don't overwrite keywords like 'while'

# Constants in Python technically don't exist. Instead, the Python community got into the consensus of naming all ...
# constants in uppercase

PI = 3.14

# Don't name your variables with an starting double underscore '__'. That could confuse someone for other type of ...
# variable.

# You also can concatenate a declaration of variables
armando, casas, alanBrito = 420, 69, 42069

print(armando, casas, alanBrito)

